class Users{
    id; name ; gender; dateOfBirth; phone ; money ; introduce; image; timestamp;


    constructor(id, name, gender, dateOfBirth, phone, money, introduce, image, timestamp) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.dateOfBirth = dateOfBirth;
        this.phone = phone;
        this.money = money;
        this.introduce = introduce;
        this.image = image;
        this.timestamp = timestamp;
    }


}